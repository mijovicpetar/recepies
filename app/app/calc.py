"""Calc module."""


def add(x: int, y: int) -> int:
    """Add two numbers.

    Args:
        x: First number.
        y: Second number.
    Returns:
        Returns int result.

    """
    return x + y


def sub(x: int, y: int) -> int:
    """Substract two numbers.

    Args:
        x: First number.
        y: Second number.
    Returns:
        Returns int result.

    """
    return x - y
